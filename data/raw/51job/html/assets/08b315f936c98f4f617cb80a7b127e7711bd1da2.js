// 动态生成jobContainer职位内容
function generateJobContainer() {
  jobContainerData.forEach((companyData, index) => {
    const jobCom1 = $(".jobCom1").eq(index);
    jobCom1.empty();
    
    companyData.subsidiaries.forEach(subsidiary => {
      // 添加子公司标题
      if (subsidiary.name !== companyData.company) {
        const subsidiaryHtml = `
          <div class="title-container">
            <div class="line line-left"></div>
            <div class="dot"></div>
            <div class="title-text">${subsidiary.name}</div>
            <div class="dot"></div>
            <div class="line line-right"></div>
          </div>
        `;
        jobCom1.append(subsidiaryHtml);
      }
      
      // 添加职位
      subsidiary.jobs.forEach(job => {
        const jobHtml = `
          <div class="jobname">
            <p>${job.title}</p>
            <p>点击查看 <img src="images/arr.png" class="showArr" alt=""></p>
          </div>
          <div class="jobinfo" style="width: 580px;">
            <p class="p2-1">城市：</p>
            <p class="p2-2">${job.city}</p>
            <p class="p2-1">岗位职责：</p>
            <p class="p2-2">${job.responsibilities}</p>
            <p class="p2-1">任职条件：</p>
            <p class="p2-2">${job.requirements}</p>
            <a href="${job.applyUrl}" class="submit"><img src="images/submit.png" alt=""></a>
          </div>
        `;
        jobCom1.append(jobHtml);
      });
    });
  });
}

