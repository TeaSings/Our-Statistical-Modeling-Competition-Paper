  // 职位详情展开/折叠（使用事件委托）
  $(document).on("click", ".jobname", function(){
    const jobname = $(this);
    const showArr = jobname.find(".showArr");
    const jobinfo = jobname.next(".jobinfo");
    
    if(showArr.hasClass("arr")){
      showArr.removeClass("arr");
    }else{
      showArr.addClass("arr");
    }
    jobinfo.toggle(500);
  });
  
  // 左侧公司切换
  $(".jobLeft>p").click(function(){
    const index = $(this).index(".jobLeft>p");
    $(".jobLeft>p").eq(index).addClass("act1").siblings().removeClass("act1");
    $(".jobCom1").eq(index).show().siblings().hide();
  });
  
  // 左侧公司切换（jobLeft1）
  $(".jobLeft1>p").click(function(){
    const index = $(this).index(".jobLeft1>p");
    $(".jobLeft1>p").eq(index).addClass("act1").siblings().removeClass("act1");
    $(".jobCom2").eq(index).show().siblings().hide();
  });
